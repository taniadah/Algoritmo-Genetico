    private void btnIniciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciaActionPerformed
      int tam = Integer.parseInt(jtfTamPob.getText());
      int nGen = Integer.parseInt(jtfGeneraciones.getText());
      if(tam==0 || nGen==0){
       JOptionPane.showMessageDialog(null, "el tamaño de la población y el numero de generaciones no pueden ser 0", "Error", JOptionPane.ERROR_MESSAGE);
      }else{
        jtfTamPob.setEnabled(false);
        String cad = jtfCadena.getText();
        jtfCadena.setEnabled(false);
        jtfGeneraciones.setEnabled(true);
        ag = new AlgoritmoGenetico(tam, cad, nGen);
        ag.inicializa();
      }


        // TODO add your handling code here:
    }//GEN-LAST:event_btnIniciaActionPerformed

