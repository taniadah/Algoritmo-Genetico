    private void jtfTamPobKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfTamPobKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!Character.isDigit(c) || (Character.isDigit(c) && jtfTamPob.getText().startsWith("0"))) {
            evt.consume();
        }
        if(c == '.' &&  jtfTamPob.getText().contains(".")){
            evt.consume();
        }
    }//GEN-LAST:event_jtfTamPobKeyTyped

